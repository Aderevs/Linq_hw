using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Linq_hw
{
    internal static class Calculator
    {
        static public dynamic Add(dynamic x, dynamic y)
        {
            return x + y;
        }
        static public dynamic Sub(dynamic x, dynamic y)
        {
            return x - y;
        }
        static public dynamic Mul(dynamic x, dynamic y)
        {
            return x * y;
        }
        static public dynamic Div(dynamic x, dynamic y)
        {
            return x / y;
        }
    }
}
